CREATE OR REPLACE FUNCTION "public"."semantic_memory_search_pgvector"("p_channel_id" "text", "p_query_embedding" "public"."vector", "p_limit" integer, "p_search_limit" integer) RETURNS TABLE("entry_id" "uuid", "memory_type" "text", "content" "text", "tags" "text"[], "context" "jsonb", "embedding" "public"."vector", "created_at" timestamp with time zone, "updated_at" timestamp with time zone, "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
begin
    return query
    select
        s.entry_id,
        s.memory_type,
        s.content,
        s.tags,
        s.context,
        s.embedding,
        s.created_at,
        s.updated_at,
        1 - (s.embedding <=> p_query_embedding) as similarity
    from public.semantic_memory_entries s
    where s.channel_id = p_channel_id
    order by similarity desc
    limit p_limit;
end;
$$;


